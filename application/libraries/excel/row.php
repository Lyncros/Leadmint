<?php
/**
 * Generate object row
 * This objet contain objects cell with values
 *
 * @author Pagnone Pablo
 * @version 0.7
 */
class Row{
    
    /**
     * Cell from row
     *
     * @var Row
     */
    public $cells;
       
    /**
     * Add cell to row
     *
     * @param Cell $cell
     */
    public function addCell(Cell $cell){
        
        $this->cells[]= $cell;
        
    }
    
}
?>