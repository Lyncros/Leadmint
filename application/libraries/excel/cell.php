<?php
/**
 * Generate object cell 
 * Set type cell(string,number or datetime) and his value
 *
 * @author Pagnone Pablo
 * @version 0.7
 */
class Cell{
    
    /**
     * Type of cell
     *
     * @var string
     */
    public $type;
    
    /**
     * Value of cell
     *
     * @var string|integer
     */
    public $value;
    
    public  function __construct(CellType $type,$value){
        
        $this->type = $type->getType();
        $this->value = $value;     
        
           
    }
    
    
    
    
}
?>